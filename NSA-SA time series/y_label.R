label_in_y <- function(y_label){
  if(y_label == 'NULL'){
    ay <- list(title = "",
               zeroline = FALSE,
               showline = FALSE,
               showticklabels = FALSE,
               showgrid = FALSE)
    return(ay)
  }
  else{
    ay <- list(title = "",
               zeroline = FALSE,
               showline = FALSE,
               showticklabels = FALSE,
               showgrid = FALSE)
    return(ay)
  } 
}
